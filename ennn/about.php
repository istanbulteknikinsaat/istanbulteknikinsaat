<br><br>
İstanbul Teknik exports to 53 conturies including Europe, Turkic Rebuplics and Middle East Countries. İstanbul Teknik offers relevant solutions to its customers with over 20 engineers and architects, 80 employees and 1.000 selling point around Turkey.
