<h3>How Geoarme is applied?</h3>

<img src="<?php echo $imgDir?>geoarme-nasil-uygulanir-hendek-kazilir.jpg" alt="Hendek kazılır, <?php echo $title.', '.$breadcrumpTitle1.', '.$breadcrumpTitle2; ?>">
<p><br>A trench is dug. By compressing gravels, a small base is formed in the bottom of the trench. The first raw blocks are stacked on it. Drainage pipe is placed to the inside of the blocks and the trench is filled with granular material.
