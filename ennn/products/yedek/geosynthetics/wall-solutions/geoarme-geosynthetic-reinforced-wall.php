<img src="<?php echo $imgDir?>geoarme-nasil-uygulanir-fortex-gg-serilir.jpg" alt="Fortex GG Geogrid serilir, <?php echo $title.', '.$breadcrumpTitle1.', '.$breadcrumpTitle2; ?>">
